public static void main(String[] args) {
    // Integer variable
    int count = 10;
    count += 1;
    System.out.println(count);

    // Float variable
    float price = 420.99f; // 'f' suffix indicates a float literal
    price = price - 0.30f; // 'f' suffix for float literals
    System.out.println(price);

    // String variable
    String message = "What the sigma";
    message += " fr fr";
    System.out.println(message);

    // Boolean variable
    boolean isLeet = true;
    isLeet = !isLeet;
    System.out.println(isLeet);
}
